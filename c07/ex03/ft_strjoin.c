char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
		i++;
	while (src[j] != '\0')
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strjoin(int size, char **strs, char *sep)
{
	int	i;
	char	*string;

	i = 0;
	while (i < size)
	{
		ft_strcat(string, (*strs)[i]);
		if (i != (size - 1))
			ft_strcat(string, sep);
	}
}