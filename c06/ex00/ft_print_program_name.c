#include <unistd.h>

int	main(int argc, char **argv)
{
	unsigned int	j;

	j = 0;
	(void)argc;
	while (argv[0][j])
		j++;
	write(1, argv[0], j);
	write(1, "\n", 1);
	return (0);
}	
